    public void showrecords(){
     try {
            pst=con.prepareStatement("SELECT * from fees order by receipt_no desc limit 1"); /*fetch the records in descending order*/
             rs=pst.executeQuery();
             rs.next();
             txt_receiptno.setText(rs.getString("receipt_no"));
             txt_receivedfrom.setText(rs.getString("student_name"));
             txt_paymentmode.setText(rs.getString("payment_mode"));
             String payment_mode=rs.getString("payment_mode");
             
             if(payment_mode.equalsIgnoreCase("Cash")){
                 lbl_cheque.setText("cheque_no");
                 txt_cheque_dd.setText("-");
                 txt_bankname.setText("-");
             }
             if(payment_mode.equalsIgnoreCase("Cheque")){
                 lbl_cheque.setText("cheque_no");
                 txt_cheque_dd.setText(rs.getString("cheque_no"));
                 txt_bankname.setText(rs.getString("bank_name"));
             }
             
             if(payment_mode.equalsIgnoreCase("Demand Draft")){
                 lbl_cheque.setText("dd_no");
                 txt_cheque_dd.setText(rs.getString("dd_no"));
                 txt_bankname.setText(rs.getString("bank_name"));
             }
             
             if(payment_mode.equalsIgnoreCase("Card")){
                 lbl_cheque.setText("-");
                 txt_cheque_dd.setText("-");
                 txt_bankname.setText(rs.getString("bank_name"));
             }
             
             txt_gstin.setText(rs.getString("gstin"));
             txt_semester.setText(rs.getString("semester"));
             txt_year1.setText(rs.getString("year1"));
             txt_year2.setText(rs.getString("year2"));
             txt_coursename.setText(rs.getString("course_name"));
             txt_amount.setText(rs.getString("amount"));
             txt_cgstamount.setText(rs.getString("cgst"));
             txt_sgstamount.setText(rs.getString("sgst"));
             txt_total.setText(rs.getString("total_amount"));
             txt_amountword.setText(rs.getString("total_amount_word"));
             txt_date.setText(rs.getString("date"));
                    } catch (SQLException ex) {
            Logger.getLogger(addfee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
