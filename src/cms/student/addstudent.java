    public int rollno() {
        int rollno = 0;
        try {
            pst = con.prepareStatement("SELECT MAX(rollno) from students");
            rs = pst.executeQuery();
            if (rs.next() == true) {
                rollno = rs.getInt(1);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return rollno + 1;
    }

