    private void choose_pic_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choose_pic_bttnActionPerformed
        JFileChooser picchooser=new JFileChooser();
        picchooser.setDialogTitle("Select Image");
        picchooser.showOpenDialog(null);
        File pic=picchooser.getSelectedFile();
        path= pic.getAbsolutePath();
        ImageIcon icon=new ImageIcon(new ImageIcon(path).getImage().getScaledInstance(lbl_image.getWidth(), lbl_image.getHeight(), Image.SCALE_SMOOTH));
        lbl_image.setIcon(icon);
        try{
        File image = new File(path);
        FileInputStream fis = new FileInputStream(image);
        ByteArrayOutputStream baos= new ByteArrayOutputStream();
        byte[] buff = new byte[1024];
        for(int readNum; (readNum=fis.read(buff)) !=-1 ; ){
            baos.write(buff,0,readNum);
            }
        userimage=baos.toByteArray();
    }
    catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
    }     
    }//GEN-LAST:event_choose_pic_bttnActionPerformed

