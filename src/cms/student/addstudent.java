    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
       String firstname=txt_fname.getText();
       String lastname=txt_lname.getText();
       SimpleDateFormat sdf=new SimpleDateFormat("YYYY-MM-dd"); 
       String dob=sdf.format(txt_dob.getDate());
       String email=txt_mail.getText();
       String address=txt_address.getText();
       String contact=txt_phone.getText();
       String admissiondate=txt_doa.getText();
       int rollno=Integer.parseInt(txt_roll.getText());
       String cource=courcetype.getSelectedItem().toString();
       String fathername=txt_father.getText();
       String fatheroccupation=txt_foccupation.getText();
       String mothername=txt_mother.getText();
       String motheroccupation=txt_moccupation.getText();
       String parent_contact=txt_contact.getText();
       
       try {
            pst = con.prepareStatement("insert into students (firstname,lastname,dob,gender,email,address,contact,admissiondate,rollno,cource,fathername,fatheroccupation,mothername,motheroccupation,parent_contact,pic)values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?);");
            pst.setString(1, firstname);
            pst.setString(2, lastname);
            pst.setString(3, dob);
            pst.setString(4, gender);
            pst.setString(5, email);
            pst.setString(6, address);
            pst.setString(7, contact);
            pst.setString(8, admissiondate);
            pst.setInt(9, rollno);
            pst.setString(10, cource);
            pst.setString(11, fathername);
            pst.setString(12, fatheroccupation);
            pst.setString(13, mothername);
            pst.setString(14, motheroccupation);
            pst.setString(15, parent_contact);
            pst.setBytes(16, userimage);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Student Added Successfully!!");
            
            /**
            * coding for clearing the form fname.setText("");
            * lname.setText(""); mail.setText(""); txt_user.setText("");
            * security_ques.setSelectedIndex(-1); security_ans.setText("");
            * pass.setText(""); fname.requestFocus();
            */

        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitActionPerformed

