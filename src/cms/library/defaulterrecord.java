public void issuebookrecord() {
   Long l= System.currentTimeMillis();
   Date todaydate=new Date(l);
        try {
            pst = con.prepareStatement("SELECT * FROM issue_book  where due_date <? and status=?");
            pst.setDate(1, todaydate);
            pst.setString(2, "Pending");
            rs = pst.executeQuery();
            while (rs.next()) {
                String id = rs.getString("id");
                String bookname = rs.getString("book_name");
                String studentname = rs.getString("student_name");
                String semester = rs.getString("semester");
                String issuedate = rs.getString("issue_date");
                String duedate = rs.getString("due_date");
                String status = rs.getString("status");
                Object[] obj = {id, bookname, studentname, semester, issuedate, duedate, status};
                d = (DefaultTableModel) issue_book_table.getModel();
                d.addRow(obj);
            }
        } catch (Exception e) {

        }
    }
