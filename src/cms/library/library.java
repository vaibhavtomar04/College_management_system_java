    public void updatestudenttable() {
        try {
            pst = con.prepareStatement("SELECT * FROM students");
            rs = pst.executeQuery();
            while (rs.next()) {
                String rollno = rs.getString("rollno");
                String firstname = rs.getString("firstname");
                String lastname = rs.getString("lastname");
                String course = rs.getString("cource");
                String contact = rs.getString("contact");
                Object[] obj = {rollno, firstname, lastname, course, contact};
                d = (DefaultTableModel) student_table.getModel();
                d.addRow(obj);
            }
        } catch (Exception e) {

        }
    }

    /**
     * ******************************************************************************************************
     */
    public void updatebooktable() {
        try {
            pst = con.prepareStatement("SELECT * FROM book_detail");
            rs = pst.executeQuery();
            while (rs.next()) {
                String bookid = rs.getString("bookid");
                String name = rs.getString("book_name");
                String author = rs.getString("author");
                String quantity = rs.getString("quantity");
                Object[] obj = {bookid, name, author, quantity};
                d = (DefaultTableModel) book_detail.getModel();
                d.addRow(obj);
            }
        } catch (Exception e) {

        }
    }

