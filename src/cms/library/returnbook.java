    /*************************************fetch issue book details**********************************************************************/
    public void issuebookdeatils(){
      int bookid=Integer.parseInt(txt_bookid.getText());
      int rollno=Integer.parseInt(txt_studentroll.getText());
        try {
            pst=con.prepareStatement("select * from issue_book where book_id=? and student_roll=? and status=?");
            pst.setInt(1, bookid);
            pst.setInt(2, rollno);
            pst.setString(3, "Pending");
            rs=pst.executeQuery();
            if(rs.next()){
              lbl_issueid.setText(rs.getString("book_id"));
              lbl_bookname.setText(rs.getString("book_name"));
            lbl_studentname.setText(rs.getString("student_name"));
            lbl_semester.setText(rs.getString("semester"));
            lbl_issuedate.setText(rs.getString("issue_date"));
            lbl_duedate.setText(rs.getString("due_date"));
            lbl_bookerror.setText("");
            }else{
              lbl_bookerror.setText("NO Record Found");
               lbl_issueid.setText("");
              lbl_bookname.setText("");
            lbl_studentname.setText("");
            lbl_semester.setText("");
            lbl_issuedate.setText("");
            lbl_duedate.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(returnbook.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    
    }
    /*************************************Return the book*********************************************************************/
    public boolean returnbook(){
        boolean status=false;
      int bookid=Integer.parseInt(txt_bookid.getText());
      int rollno=Integer.parseInt(txt_studentroll.getText());
        try {
            pst=con.prepareStatement("update issue_book set status=? where student_roll=? and book_id=? and status=?");
            pst.setString(1, "Returned");
            pst.setInt(2, rollno);
            pst.setInt(3, bookid);
            pst.setString(4, "Pending");
           int row=pst.executeUpdate();
            if(row>0){
              status=true;
            }else{
              status=false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(returnbook.class.getName()).log(Level.SEVERE, null, ex);
        }
        return status;
    }
