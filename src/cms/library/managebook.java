    public void updatebooktable(){
          try{
         pst=con.prepareStatement("SELECT * FROM book_detail");
         rs=pst.executeQuery();
             while(rs.next()){
                  int bookid=rs.getInt("bookid");
                 String book_name=rs.getString("book_name");
                 String author=rs.getString("author");
                 int quantity=rs.getInt("quantity");
                Object[] obj={bookid,book_name,author,quantity};
                d=(DefaultTableModel)book_detail.getModel();
                d.addRow(obj);
             }
      }catch(Exception e){
      
      }
    }
