    private void add_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_bttnActionPerformed
         int bookid= Integer.parseInt(txt_bid.getText());
        String book_name= txt_bname.getText();
         String author= txt_author.getText();
         int quantity= Integer.parseInt(txt_quantity.getText());
             try {
                 pst=con.prepareStatement("insert into book_detail values(?,?,?,?)");
            pst.setInt(1, bookid);
            pst.setString(2, book_name);
            pst.setString(3, author);
            pst.setInt(4, quantity);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Book Added Successfully!!");
            cleartable();
            txt_bid.setText("");
            txt_bname.setText("");
            txt_author.setText("");
            txt_quantity.setText("");
            txt_bid.requestFocus();
             } catch (SQLException ex) {
                 Logger.getLogger(managebook.class.getName()).log(Level.SEVERE, null, ex);
             }
         updatebooktable();
    }//GEN-LAST:event_add_bttnActionPerformed

    private void update_bttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_bttnActionPerformed
        int bookid=Integer.parseInt(txt_bid.getText());
        String book_name= txt_bname.getText();
        String author=txt_author.getText();
         int quantity=Integer.parseInt(txt_quantity.getText());
        try {
            pst= con.prepareStatement("update book_detail set book_name=? , author=?  , quantity=? where bookid=?");
            pst.setString(1,book_name);
            pst.setString(2,author);
            pst.setInt(3, quantity);
            pst.setInt(4, bookid);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Book Edited Successfully!!");
            cleartable();
            txt_bid.setText("");
            txt_bname.setText("");
            txt_author.setText("");
            txt_quantity.setText("");
            txt_bid.requestFocus();
        } catch (SQLException ex) {
            Logger.getLogger(managebook.class.getName()).log(Level.SEVERE, null, ex);
        }
updatebooktable();
    }//GEN-LAST:event_update_bttnActionPerformed

