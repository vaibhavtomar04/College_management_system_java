    /**
     * *************************************fetch details using date*******************************************
     */
    public void search() {
        Date from = fromdate.getDatoFecha();
        Date to = todate.getDatoFecha();

        Long a = from.getTime();
        /*fetch time from util in long format*/
        Long b = to.getTime();
        java.sql.Date fromdate = new java.sql.Date(a);
        /**
         * convert util date to sql date as database support only sql date*
         */
        java.sql.Date todate = new java.sql.Date(b);

        try {
            pst = con.prepareStatement("select * from issue_book where issue_date BETWEEN ? and ?");
            pst.setDate(1, fromdate);
            pst.setDate(2, todate);
            rs = pst.executeQuery();
            if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "No Record Found!!!");
            } else {
                while (rs.next()) {
                    String id = rs.getString("id");
                    String bookname = rs.getString("book_name");
                    String studentname = rs.getString("student_name");
                    String semester = rs.getString("semester");
                    String issuedate = rs.getString("issue_date");
                    String duedate = rs.getString("due_date");
                    String status = rs.getString("status");
                    Object[] obj = {id, bookname, studentname, semester, issuedate, duedate, status};
                    d = (DefaultTableModel) search_record.getModel();
                    d.addRow(obj);
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(viewrecord.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

