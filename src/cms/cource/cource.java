    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        int id=Integer.parseInt(lbl_id.getText());
        String courcename= lbl_cname.getText();
        double courcecost= Double.parseDouble(lbl_ccost.getText());

        try {
            pst= con.prepareStatement("update cource set courcename=? , courcecost=?  where id=?");
            pst.setString(1,courcename);
            pst.setDouble(2,courcecost);
            pst.setInt(3, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Cource Edited Successfully!!");
            cleartable();

        } catch (SQLException ex) {
            Logger.getLogger(cource.class.getName()).log(Level.SEVERE, null, ex);
        }
updatetable();
    }//GEN-LAST:event_updateActionPerformed

