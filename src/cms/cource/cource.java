             public void updatetable(){
      try{
         pst=con.prepareStatement("SELECT * from cource");
             rs=pst.executeQuery();
             while(rs.next()){
                 String courceid=rs.getString("id");
                 String courcename=rs.getString("courcename");
                 String courcecost=rs.getString("courcecost");
                Object[] obj={courceid,courcename,courcecost};
                d=(DefaultTableModel)table_cource.getModel();
                d.addRow(obj);
             }
      }catch(Exception e){
      
      }
    }
